#include <stdlib.h>

int	ft_init(char **str, int size)
{
	if (size == 0)
	{
		*str = malloc(1 * sizeof(char));
		if (!*str)
			return (1);
		(*str)[0] = '\0';
		return (1);
	}
	return (0);
}